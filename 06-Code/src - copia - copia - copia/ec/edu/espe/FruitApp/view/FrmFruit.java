    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        Fruit fruit;
        String name;
        String texture;
        int quantity;
        double weight;
        float cost;
        
        FruitController fruitController;
        fruitController = new FruitController();
        
        name = cmbName.getSelectedItem().toString();
        texture = cmbTexture.getSelectedItem().toString();
        quantity = Integer.parseInt(spnQuantity.getValue().toString());
        weight = Double.parseDouble(txtWeight.getText());
        cost = Float.parseFloat(txtCost.getText());
        
        fruit = new Fruit(name, texture, quantity, weight, cost);
        
        fruitController.register(fruit, this);
        ConexionMongoDBFruit conexionMongoDb = new ConexionMongoDBFruit();
        conexionMongoDb.ConexionFruit(name, texture, quantity, weight, cost);
    }//GEN-LAST:event_btnAddActionPerformed

